<?php

namespace App\Core;

class Model {

    //vamos aplicar o padr達o de projeto Singleton
    private static $conexao;


    public static function getConexao(){

        $host = $_ENV["database_host"];
        $user = $_ENV["database_user"];
        $password = $_ENV["database-pass"];

        //se a conex達o n達o estiver criada, criamos ela
        if(!isset(self::$conexao)){
            self::$conexao = new \PDO("mysql:host=$host;port=3306;dbname=fastParking;", $user, $password);
        }
        
        //retornamos a conex達o
        return self::$conexao;
    }

}